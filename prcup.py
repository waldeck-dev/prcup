def group_by_hundred(numbers):
    result = {}
    for num in numbers:
        hundred = (num // 100) * 100
        result.setdefault(hundred, []).append(num)
    return result


hundred_dict = group_by_hundred(NUMBERS)


final = [5000]

for n in range(22, 50):
    n00 = n * 100
    out = set(hundred_dict.get(n00, []))
    out.add(n00)
    out.add(int(f"{n}{n}"))
    out.add(int(f"{n}{str(n)[1]}{str(n)[0]}"))

    a = int(str(n)[0])
    b = int(str(n)[1])
    c = (b + 1) % 10
    d = (c + 1) % 10
    out.add(int(f"{a}{b}{c}{d}"))

    out.add(int(f"{a}{str(b) *3 }"))

    while len(out) < 8:
        out.add(random.randint(n00, n00 + 99))

    final += list(out)

print(",".join(map(lambda i: str(i), sorted(final))))
